console.log(null===undefined); // Output: false
console.log(null==undefined); // Output: true

//In JavaScript, null and undefined are two distinct types. null represents the intentional absence of any object value, while undefined represents a variable that has been declared but has not been assigned a value. When using the strict equality operator (===), null and undefined are not considered equal because they are of different types. However, when using the loose equality operator (==), null and undefined are considered equal because they both represent the absence of a value.

