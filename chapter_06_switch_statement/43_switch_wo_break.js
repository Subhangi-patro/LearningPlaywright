//switch
// 0 - sunday,1-monday,2-tuesday,3-wednesday,4-thursday,5-friday,6-saturday
let day = 3;
switch (day) {
    case 0:
        console.log("sunday"); 
    case 1:
        console.log("monday");
    case 2:
        console.log("tuesday");     
    case 3:
        console.log("wednesday");
    case 4:
        console.log("thursday");
    case 5:
        console.log("friday");
    case 6:
        console.log("saturday");
    default:
        console.log("invalid day");
}